namespace ManifoldNET;

/// <summary>
/// Int vector3.
/// </summary>
public struct IntVector3
{
    /// <summary>
    /// X value.
    /// </summary>
    public int x;

    /// <summary>
    /// Y value.
    /// </summary>
    public int y;

    /// <summary>
    /// Z value.
    /// </summary>
    public int z;

    /// <summary>
    /// Initializes a new instance of <see cref="IntVector3"/>.
    /// </summary>
    public IntVector3(int x, int y, int z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
